    #region ZeroitH1    
    /// <summary>
    /// A class collection for rendering an H1 Label.
    /// </summary>
    /// <seealso cref="System.Windows.Forms.Label" />
    [Designer(typeof(ZeroitLabelH1Designer))]
    public class ZeroitLabelH1 : Label
    {

        #region " Private "
        /// <summary>
        /// The g
        /// </summary>
        private Graphics G;
        #endregion

        #region " Control "

        /// <summary>
        /// Initializes a new instance of the <see cref="ZeroitLabelH1"/> class.
        /// </summary>
        public ZeroitLabelH1()
        {
            DoubleBuffered = true;
            AutoSize = false;
            Font = new Font("Segoe UI Semibold", 20);
            ForeColor = Color.FromArgb(76, 88, 100);
        }


        /// <summary>
        /// Raises the <see cref="E:System.Windows.Forms.Control.Paint" /> event.
        /// </summary>
        /// <param name="e">A <see cref="T:System.Windows.Forms.PaintEventArgs" /> that contains the event data.</param>
        protected override void OnPaint(PaintEventArgs e)
        {
            G = e.Graphics;
            G.SmoothingMode = SmoothingMode.HighQuality;
            G.TextRenderingHint = TextRenderingHint.ClearTypeGridFit;

            base.OnPaint(e);

            using (Pen P = new Pen(Helpers.GreyColor(200)))
            {
                G.DrawLine(P, new Point(0, 50), new Point(Width, 50));
            }

        }

        #endregion

    }


    #region Smart Tag Code

