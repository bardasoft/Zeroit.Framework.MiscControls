    #region Panel    
    /// <summary>
    /// A class collection for rendering a panel.
    /// </summary>
    /// <seealso cref="System.Windows.Forms.ContainerControl" />
    [Designer(typeof(ZeroitPanelDefaultDesigner))]
    public class ZeroitPanelDefault : ContainerControl
    {
        #region Variables
        /// <summary>
        /// The border
        /// </summary>
        private Color border = Color.FromArgb(180, 180, 180);

        #region Brush Enum
        /// <summary>
        /// The background
        /// </summary>
        private Color background = Color.White;

        #endregion

        #endregion

        #region Properties        
        /// <summary>
        /// Gets or sets the type of the brush.
        /// </summary>
        /// <value>The type of the brush.</value>
        public Color BrushType
        {
            get { return background; }
            set
            {
                background = value;
                Invalidate();
            }
        }

        /// <summary>
        /// Gets or sets the border.
        /// </summary>
        /// <value>The border.</value>
        public Color Border
        {
            get { return border; }
            set
            {
                border = value;
                Invalidate();
            }
        }




        #endregion


        /// <summary>
        /// The shape
        /// </summary>
        private GraphicsPath Shape;

        /// <summary>
        /// Initializes a new instance of the <see cref="ZeroitPanelDefault" /> class.
        /// </summary>
        public ZeroitPanelDefault()
        {
            SetStyle(ControlStyles.SupportsTransparentBackColor, true);
            SetStyle(ControlStyles.UserPaint, true);

            BackColor = Color.Transparent;
            this.Size = new Size(187, 117);
            Padding = new Padding(5, 5, 5, 5);
            DoubleBuffered = true;
        }

        /// <summary>
        /// Raises the <see cref="E:System.Windows.Forms.Control.Resize" /> event.
        /// </summary>
        /// <param name="e">An <see cref="T:System.EventArgs" /> that contains the event data.</param>
        protected override void OnResize(System.EventArgs e)
        {
            base.OnResize(e);

            Shape = new GraphicsPath();
            var _with1 = Shape;
            _with1.AddArc(0, 0, 10, 10, 180, 90);
            _with1.AddArc(Width - 11, 0, 10, 10, -90, 90);
            _with1.AddArc(Width - 11, Height - 11, 10, 10, 0, 90);
            _with1.AddArc(0, Height - 11, 10, 10, 90, 90);
            _with1.CloseAllFigures();
        }

        /// <summary>
        /// Raises the <see cref="E:System.Windows.Forms.Control.Paint" /> event.
        /// </summary>
        /// <param name="e">A <see cref="T:System.Windows.Forms.PaintEventArgs" /> that contains the event data.</param>
        protected override void OnPaint(System.Windows.Forms.PaintEventArgs e)
        {
            base.OnPaint(e);
            Bitmap B = new Bitmap(Width, Height);
            Graphics G = Graphics.FromImage(B);


            G.SmoothingMode = SmoothingMode.HighQuality;

            G.Clear(Color.Transparent);

            G.FillPath(new SolidBrush(background), this.Shape);


            
            // Draw RTB background
            G.DrawPath(new Pen(border), Shape); // Draw border

            G.Dispose();
            e.Graphics.DrawImage((Image)B.Clone(), 0, 0);
            B.Dispose();
        }
    }


